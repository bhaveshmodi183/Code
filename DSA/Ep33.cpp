#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin>>n;
    int a[n];
    for(int i=0;i<n;i++)
    {
        cin>>a[i];
    }
    sort(a,a+n);
    for(int i=0;i<n;i++)
    {
        cout<<a[i]<<" ";
    }
}

// COMPLEXITY OF SORT FUNCTION IN C++ IS O(nlogn)

// COMPARATOR 
//   syntax:    sort(starting point, end point , comparator function);